package com.entitly;

public class ShangPin {
	private int xid;
	private String name;
	public ShangPin() {
	}
	public ShangPin(int xid, String name) {

		this.xid = xid;
		this.name = name;
	}
	public int getXid() {
		return xid;
	}
	public void setXid(int xid) {
		this.xid = xid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
